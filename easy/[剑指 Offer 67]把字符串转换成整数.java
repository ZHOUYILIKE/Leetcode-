class Solution {
    public int strToInt(String str) {

        //æœ¬é¢˜å¾—è¾¹ç•Œé—®é¢˜ç‰¹åˆ«è®©æˆ‘å¤´ç–¼ï¼Œè¿˜æ˜¯æ²¡èƒ½é™ä¸‹å¿ƒæŠŠæ‰€æœ‰çš„ç»†èŠ‚æ€§çš„ç‚¹éƒ½æ‰¾åˆ°å°±å¼€å§‹å†™ä»£ç 
        //è¿™æ ·å¯¼è‡´èŠ±äº†å¾ˆé•¿æ—¶é—´éƒ½æ”¹ä¸å¯¹ä»£ç ï¼Œé€šè¿‡ä¸äº†
        //æœ¬é¢˜æœ€é‡è¦çš„ç‚¹å°±æ˜¯å¤„ç†è¾¹ç•Œæº¢å‡ºçš„é—®é¢˜

        int num = 0;
        int flag = 0;
        int sign = 1;
        int boundray = Integer.MAX_VALUE/10;
        String trim = str.trim();
        if(trim.length()>0){
            if (trim.charAt(0)=='-'){
                sign = -sign;
                flag = 1;
            }else if (trim.charAt(0)=='+'){
                flag = 1;
            }
            while (trim.length()>flag&&Character.isDigit(trim.charAt(flag))){

                if (num > boundray || num == boundray && trim.charAt(flag)>'7'){
                    return sign==-1?Integer.MIN_VALUE:Integer.MAX_VALUE;
                }
                num = num*10 + Integer.parseInt(String.valueOf(trim.charAt(flag)));

                flag++;
            }
        }
        return sign * num;
    }
}
